print('Calculator🧮\n ')

num1 = int(input('Enter your first number:')) 
num2 = int(input('Enter your seconds number:'))
operator = input('Choose your operator(+ - * % / //) ')

if operator == '+':
 print(num1 + num2)

elif operator == '-':
 print(num1 - num2)

elif operator == '*':
 print(num1 * num2)

elif operator == '%':
 print(num1 % num2)

elif operator == '/':
 print(num1 / num2)

elif operator == '//':
 print(num1 // num2)












































